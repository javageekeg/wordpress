if ( ! function_exists( 'eduprime_setup' ) ) :
    /**
     * Sets up theme defaults and registers support for various WordPress features.
     *
     * Note that this function is hooked into the after_setup_theme hook, which
     * runs before the init hook. The init hook is too late for some features, such
     * as indicating support for post thumbnails.
     */
    function eduprime_setup() {
        // Make theme available for translation.
        load_theme_textdomain( 'eduprime', get_template_directory() . '/languages' );

        // Add default posts and comments RSS feed links to head.
        add_theme_support( 'automatic-feed-links' );

        // Let WordPress manage the document title.
        add_theme_support( 'title-tag' );

        // Enable support for Post Thumbnails on posts and pages.
        add_theme_support( 'post-thumbnails' );

        // Register navigation menus.
        register_nav_menus( array(
            'primary' => esc_html__( 'Primary Menu', 'eduprime' ),
        ) );

        // Switch default core markup for search form, comment form, and comments to output valid HTML5.
        add_theme_support( 'html5', array(
            'search-form',
            'comment-form',
            'comment-list',
            'gallery',
            'caption',
        ) );

        // Set up the WordPress core custom background feature.
        add_theme_support( 'custom-background', apply_filters( 'eduprime_custom_background_args', array(
            'default-color' => 'ffffff',
            'default-image' => '',
        ) ) );

        // Add theme support for selective refresh for widgets.
        add_theme_support( 'customize-selective-refresh-widgets' );

        // Add support for core custom logo.
        add_theme_support( 'custom-logo', array(
            'height'      => 250,
            'width'       => 250,
            'flex-width'  => true,
            'flex-height' => true,
        ) );

        // Add theme support for Tutor LMS.
        add_theme_support( 'tutor-lms' );

        // Add theme support for WooCommerce.
        add_theme_support( 'woocommerce' );

        // Enable support for Custom Header.
        add_theme_support( 'custom-header', apply_filters( 'eduprime_custom_header_args', array(
            'default-image'      => '',
            'default-text-color' => '000000',
            'width'              => 1000,
            'height'             => 250,
            'flex-height'        => true,
        ) ) );
    }
endif;
add_action( 'after_setup_theme', 'eduprime_setup' );

/**
 * Set the content width in pixels, based on the theme's design and stylesheet.
 *
 * Priority 0 to make it available to lower priority callbacks.
 *
 * @global int $content_width
 */
function eduprime_content_width() {
    $GLOBALS['content_width'] = apply_filters( 'eduprime_content_width', 640 );
}
add_action( 'after_setup_theme', 'eduprime_content_width', 0 );

